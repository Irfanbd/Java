import java.util.*;

class FirstClass {
    public static void main(String args[]) {
        int a = 3;
        int b = 2;
        int sum = a + b;
        System.out.println(sum);
        int diff = b - a;
        System.out.println(diff);
        int ans = (a * b) / (a - b);
        System.out.println(ans);
    }
}